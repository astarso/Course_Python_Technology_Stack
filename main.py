# Задание №1
"""
Количество различных чисел.

В первую строку вводится число N – количество чисел (1 ≤ N ≤ 100000).
Во вторую строку вводится через пробел N чисел, каждое не превышает
2*10e9 по модулю. Программа выясняет, сколько среди этих чисел различных
и выводит число, равное количеству различных чисел.

Пример ввода:
Введите количество чисел N: 5
Введите числа: 1 2 3 2 4

Пример вывода:
Количество различных чисел: 4
"""

N = int(input("Введите количество чисел N: "))
numbers = list(map(int, input("Введите числа: ").split()))

unique_numbers = set(numbers)
print("Количество различных чисел:", len(unique_numbers))


# Задание №2
"""
Общие числа в двух списках.

Вводятся два списка чисел, которые могут содержать до 100000 чисел каждый.
Все числа каждого списка находятся на отдельной строке. Программа выводит,
сколько чисел содержится одновременно как в первом списке, так и во втором.

Пример ввода:
Введите числа первого списка:
1
2
3
Введите числа второго списка:
2
3
4

Пример вывода:
Количество общих чисел: 2
"""

list1 = set(int(input("Введите числа первого списка:\n")) for _ in range(int(input())))
list2 = set(int(input("Введите числа второго списка:\n")) for _ in range(int(input())))

common_numbers = list1.intersection(list2)
print("Количество общих чисел:", len(common_numbers))


# Задание №3
"""
Проверка наличия числа в последовательности.

Во входную строку вводится последовательность чисел через пробел.
Для каждого числа программа выводит слово ”YES” (в отдельной строке),
если это число ранее встречалось в последовательности, или ”NO”,
если не встречалось.

Пример ввода:
Введите последовательность чисел: 1 2 3 2 4

Пример вывода:
YES
NO
NO
YES
NO
"""

sequence = list(map(int, input("Введите последовательность чисел через пробел: ").split()))
seen_numbers = set()

for num in sequence:
    if num in seen_numbers:
        print("YES")
    else:
        seen_numbers.add(num)
        print("NO")
